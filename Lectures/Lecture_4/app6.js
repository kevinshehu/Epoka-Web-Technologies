// To find the type of a variable we use typeof

console.log(typeof 'Igli')                          //String

console.log(typeof { name: 'Sidrit', age: 23 });    // Object

console.log(typeof undefined);                      //Undefined

console.log(typeof null);                           //value is null of type object

console.log(typeof function () { });                // Returns function

console.log(typeof [1,2,3,4]);                      // Returns object